int		find_len(const char *str)
{
	int	i;

	i = 0;
	while (str[i])
	{
		if (str[i] == 'd' || str[i] == 'D' || str[i] == 'x' || str[i] == 'X'
				|| str[i] == 's' || str[i] == 'S' || str[i] == 'c' || str[i] == 'C'
				|| str[i] == 'p' || str[i] == 'i' || str[i] == 'O' || str[i] == 'o'
				|| str[i] == 'u' || str[i] == 'U')
			return (i);
		i++;
	}
	return (0);
}
